#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t		n;
	size_t		i;

	n = 0;
	i = 0;
	while (src[n] != '\0')
		n++;
	if (dstsize == 0)
		return (n);
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (n);
}
